"""
21. Три единицы подряд
"""
n = int(input())

len_dp = max(3, n + 1)
dp = [0]*len_dp
dp[0] = 1
dp[1] = 2
dp[2] = 4

for i in range(3, len_dp):
	dp[i] = dp[i-1] + dp[i-2] + dp[i-3]

print(dp[n])

"""
1) ДП
Time O(n)
Memory O(n)

обобщённая формула Фибоначчи
dp[i] кол-во валидных чисел, в которых i разрядов(бит)
перебираем кол-во разрядов(бит)
"""